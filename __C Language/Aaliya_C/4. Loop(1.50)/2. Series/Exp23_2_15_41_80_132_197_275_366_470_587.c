/*23. 2 15 41 80 132 197 275 366 470 587
    OR 2+[2+13*(1)]+[2+13*(1+2)]+[2+13*(1+2+3)]+[2+13*(1+2+3+4)]+[2+13*(1+2+3+4+5)]...+[2+13*(1+2+3...+9)]*/
#include<stdio.h>
int main()
{
    int i,j,p,sum=0;
    for(i=0;i<10;i++)//i=0
    {
        p=0;
        for(j=0;j<=i;j++) p+=j;
        sum+=(2+(13*p));
    }
    printf("\nSum of series [ 2 15 41 80 132 197 275 366 470 587 ] is : %d",sum);
    return 0;
}
